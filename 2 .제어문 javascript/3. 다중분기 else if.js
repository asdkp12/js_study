var age = 20;

if (age >= 20) {
  console.log("성인입니다");
} else if (age >= 17) {
  console.log("고딩입니다");
} else if (age >= 14) {
  console.log("중딩입니다");
} else if (age >= 8) {
  console.log("초딩입니다");
} else {
  console.log("아동입니다");
}

